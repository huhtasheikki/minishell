#include "libft.h"

t_u32		ft_bitmask_u32(t_u32 n)
{
	return (~(~((t_u32)0) << n));
}

t_u64		ft_bitmask_u64(t_u64 n)
{
	return (~(~((t_u64)0) << n));
}
